package homework;

public class WhileLoop {
    public static void main(String[] args) {
        //repeat till condition meets
        //4 loops while,for,do while(ntrust
        //++ or -- incroment by one - control loop
        int time=9;
        while(time>7){
            System.out.println("Hi");
            time--; //condition
        }
        System.out.println("Value of time= "+time);

    }
}
