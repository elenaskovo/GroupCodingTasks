package class6;

public class E5SwitchStatment {
    public static void main(String[] args) {
        char gender='F';
        switch(gender){
            case'F':
                System.out.println("Female");
                break;
            case'M':
                System.out.println("Male");
                break;
            case '0':
                    System.out.println("Else");
                    break;

        }

    }


}
